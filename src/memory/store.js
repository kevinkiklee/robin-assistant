/**
 * Capture a raw event into the events firehose.
 *
 * Writes events row + embedding row (in the active profile's events surface)
 * in two queries — one transaction not strictly required because embedding
 * dedupe is keyed on event ID, which is generated by SurrealDB on insert.
 *
 * @returns {{ id, deduped: boolean }}
 */
export async function remember(db, embedder, input) {
  const {
    source,
    content,
    trust = 'trusted',
    scope = 'global',
    tags = [],
    attachments = [],
    meta,
    external_id,
  } = input;
  if (!source) throw new Error('remember: source required');
  if (!content || content.length === 0) throw new Error('remember: content required');

  // Validate attachments (open enum, but known kinds get their shape checked).
  for (const a of attachments) {
    const v = validateAttachment(a);
    if (!v.ok) throw new Error(`remember: attachment invalid: ${v.errors.join('; ')}`);
  }

  const contentHash = sha256(content);

  // Dedupe by content_hash within source — keeps integration syncs idempotent.
  const [existing] = await db
    .query(
      surql`SELECT id FROM events WHERE content_hash = ${contentHash} AND source = ${source} LIMIT 1`,
    )
    .collect();
  if (existing[0]) return { id: existing[0].id, deduped: true };

  // Normalize top-level `external_id` (sync convenience) into meta.external_id
  // — the events schema dropped the top-level column in the redesign.
  const normalizedMeta = external_id != null ? { ...(meta ?? {}), external_id } : meta;

  const fields = {
    source,
    content,
    content_hash: contentHash,
    trust,
    scope,
    tags,
    attachments,
    ...(normalizedMeta ? { meta: normalizedMeta } : {}),
  };
  const [created] = await db.query(surql`CREATE events CONTENT ${fields}`).collect();
  const row = Array.isArray(created) ? created[0] : created;
  const eventId = row.id;

  // Write embedding into the active profile's events surface.
  await writeEmbedding(db, embedder, 'events', eventId, content);

  return { id: eventId, deduped: false };
}

/**
 * Create a memo of any kind. Subjects → `about` edges, lineage → `derived_from`
 * edges, both written via relateAll in the same call site.
 *
 * @returns {{ id, deduped: boolean }}
 */
export async function note(db, embedder, kind, input) {
  if (!kind) throw new Error('note: kind required');
  const v = validateMemoKind(kind, input);
  if (!v.ok) throw new Error(`note(${kind}): ${v.errors.join('; ')}`);
  const {
    content,
    subjects = [],
    confidence,
    scope = 'global',
    tags = [],
    derived_by,
    lineage = [],
    meta = {},
  } = input;

  const spec = MEMO_KIND_REGISTRY[kind];
  const dedupByHash = spec?.dedup_by === 'content_hash';
  const contentHash = sha256(content);

  if (dedupByHash) {
    const [existing] = await db
      .query(
        surql`SELECT id FROM memos WHERE kind = ${kind} AND content_hash = ${contentHash} LIMIT 1`,
      )
      .collect();
    if (existing[0]) return { id: existing[0].id, deduped: true };
  }

  const fields = {
    kind,
    content,
    content_hash: contentHash,
    derived_by,
    scope,
    tags,
    meta,
    ...(confidence !== undefined ? { confidence } : {}),
  };
  const [created] = await db.query(surql`CREATE memos CONTENT ${fields}`).collect();
  const row = Array.isArray(created) ? created[0] : created;
  const memoId = row.id;

  await writeEmbedding(db, embedder, 'memos', memoId, content);

  // Emit `about` edges for subjects (memo → entity).
  if (subjects.length > 0) {
    const rows = subjects.map((s) => ({ from: memoId, to: s, kind: 'about' }));
    await relateAll(db, rows);
  }
  // Emit `derived_from` edges for lineage entries.
  if (lineage.length > 0) {
    const rows = lineage.map((l) => ({ from: memoId, to: l.id ?? l, kind: 'derived_from' }));
    await relateAll(db, rows);
  }

  return { id: memoId, deduped: false };
}

/**
 * Upsert a memo by its `meta.name`. Used by habits today; the primitive is
 * generic (any kind that carries `meta.name`). Increments `signal_count` and
 * refreshes `last_active` on match; creates otherwise.
 *
 * @returns {{ id, signal_increment: 0 | 1 }}
 */
export async function upsertMemoByName(db, embedder, kind, input) {
  const { name } = input;
  if (!name) throw new Error('upsertMemoByName: meta.name required (passed as input.name)');
  const [existing] = await db
    .query(surql`SELECT id FROM memos WHERE kind = ${kind} AND meta.name = ${name} LIMIT 1`)
    .collect();
  if (existing[0]) {
    const id = existing[0].id;
    await db.query(surql`UPDATE ${id} SET signal_count += 1, last_active = time::now()`).collect();
    // Also merge any new lineage as derived_from edges.
    if (input.lineage?.length > 0) {
      const rows = input.lineage.map((l) => ({
        from: id,
        to: l.id ?? l,
        kind: 'derived_from',
      }));
      await relateAll(db, rows);
    }
    return { id, signal_increment: 1 };
  }
  // Create path: ensure `meta.name` is part of the meta we pass through.
  const meta = { ...(input.meta ?? {}), name };
  const result = await note(db, embedder, kind, { ...input, meta });
  return { id: result.id, signal_increment: 0 };
}

/**
 * Relate two records with a typed edge. Registry-validated. Self-loops
 * rejected. Symmetric kinds canonicalize endpoint order. Composite-ID UPSERT
 * for idempotence; counter kinds use `SET weight += 1`.
 */
export async function relate(db, from, to, kind, opts = {}) {
  const v = validateEdge(from, to, kind);
  if (!v.ok) throw new Error(`relate: ${v.errors.join('; ')}`);
  const [cFrom, cTo] = canonicalEndpoints(from, to, kind);
  const counter = isCounterKind(kind);

  // Build a deterministic composite ID. SurrealDB v3 accepts record-as-id-element
  // via the array literal syntax — we use BoundQuery here so the parser sees
  // literal record refs in the ID position.
  const fromIdStr = recordStringId(cFrom);
  const toIdStr = recordStringId(cTo);

  if (counter) {
    const sql = `UPSERT type::record('edges', [$kind, $from, $to]) SET
      kind = $kind, from = $from, to = $to,
      weight += 1,
      last_seen = time::now()`;
    const [ret] = await db
      .query(
        new BoundQuery(sql, {
          kind,
          from: cFrom,
          to: cTo,
        }),
      )
      .collect();
    const row = Array.isArray(ret) ? ret[0] : ret;
    return { id: row?.id };
  }

  const bindings = { kind, from: cFrom, to: cTo };
  const setExprs = ['kind = $kind', 'from = $from', 'to = $to', 'last_seen = time::now()'];
  for (const [field, val] of [
    ['weight', opts.weight],
    ['valid_from', opts.valid_from],
    ['valid_until', opts.valid_until],
    ['context', opts.context],
    ['meta', opts.meta],
  ]) {
    if (val !== undefined && val !== null) {
      bindings[field] = val;
      setExprs.push(`${field} = $${field}`);
    }
  }
  const sql = `UPSERT type::record('edges', [$kind, $from, $to]) SET ${setExprs.join(', ')}`;
  const [ret] = await db.query(new BoundQuery(sql, bindings)).collect();
  const row = Array.isArray(ret) ? ret[0] : ret;
  return { id: row?.id, fromId: fromIdStr, toId: toIdStr };
}

/**
 * Bulk relate. Same semantics as `relate` but batched into one statement —
 * used by biographer to emit 8-20 edges per processed event.
 */
export async function relateAll(db, rows) {
  if (!Array.isArray(rows) || rows.length === 0) return { ids: [] };
  const ids = [];
  // SurrealDB doesn't have a multi-row UPSERT primitive for arbitrary IDs;
  // batch into one BEGIN/COMMIT block for atomicity.
  for (const row of rows) {
    const { id } = await relate(db, row.from, row.to, row.kind, row);
    ids.push(id);
  }
  return { ids };
}

/**
 * Add a `supersedes` edge new_memo → old_memo. fn::freshness will return 0
 * for old_memo from now on (per its inbound supersedes check). The old memo
 * row is NOT updated/deleted — historical readings remain queryable.
 */
export async function supersede(db, oldId, newId) {
  const { id } = await relate(db, newId, oldId, 'supersedes');
  return { superseded: oldId, by: newId, edge: id };
}

/**
 * Mark two memos as contradicting each other (symmetric, both visible).
 */
export async function flagContradiction(db, idA, idB, opts = {}) {
  return relate(db, idA, idB, 'contradicts', opts);
}

/**
 * Update a memo's `meta` field by merging in a patch. Used by foresight to
 * record prediction resolution (resolved_at / correct / actual_outcome).
 */
export async function updateMemoMeta(db, id, patch) {
  await db.query(surql`UPDATE ${id} MERGE { meta: ${patch} }`).collect();
}

