/**
 * Write the embedding for a record into the active profile's embedding table
 * for the given surface. Idempotent (UPSERT by the record's ID).
 *
 * Profile name is read from `runtime:embedder.active_profile` and validated
 * against /^[a-z0-9-]+$/ in profile-router; surface against /^events|memos|entities$/.
 * Table name is therefore safe to interpolate.
 */
async function writeEmbedding(db, embedder, surface, recordId, text) {
  const profile = await activeProfile(db);
  const table = embeddingTable(profile, surface);
  const vec = Array.from(await embedder.embed(text));
  const sql = 'UPSERT type::record($tb, [$rec]) SET record = $rec, vector = $vec, ts = time::now()';
  await db
    .query(
      new BoundQuery(sql, {
        tb: table,
        rec: recordId,
        vec,
      }),
    )
    .collect();
}

