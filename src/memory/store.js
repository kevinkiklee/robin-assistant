/**
 * Read a memo with hydrated subjects, lineage, contradictions count.
 */
export async function getMemo(db, id) {
  const [rows] = await db
    .query(surql`SELECT *, fn::freshness(id) AS freshness FROM ${id}`)
    .collect();
  const memo = rows[0];
  if (!memo) return null;
  const [subjects] = await db
    .query(surql`SELECT VALUE to FROM edges WHERE kind = 'about' AND from = ${id}`)
    .collect();
  const [lineage] = await db
    .query(surql`SELECT VALUE to FROM edges WHERE kind = 'derived_from' AND from = ${id}`)
    .collect();
  const [contraRows] = await db
    .query(
      surql`SELECT count() AS n FROM edges WHERE kind = 'contradicts' AND (from = ${id} OR to = ${id}) GROUP ALL`,
    )
    .collect();
  return {
    ...memo,
    subjects,
    lineage,
    contradictions: contraRows?.[0]?.n ?? 0,
  };
}

/**
 * HNSW kNN over memos. Filters by kind / scope(s) / tags / since are applied
 * during the SQL query (indexed where applicable). Default excludes ephemeral
 * scopes; pass `scopes: ['*']` to include everything.
 *
 * Returns `{ hits: [{ record, distance, ... }] }` — caller layers rank.score.
 */
export async function searchMemos(db, embedder, query, opts = {}) {
  return _surfaceSearch(db, embedder, 'memos', query, opts);
}

export async function searchEvents(db, embedder, query, opts = {}) {
  return _surfaceSearch(db, embedder, 'events', query, opts);
}

export async function searchEntities(db, embedder, query, opts = {}) {
  return _surfaceSearch(db, embedder, 'entities', query, opts);
}

async function _surfaceSearch(db, embedder, surface, query, opts) {
  const limit = Number.isInteger(opts.limit) ? opts.limit : 10;
  if (limit < 1 || limit > 100) {
    throw new Error(`searchMemos: limit out of range [1,100]: ${limit}`);
  }
  const ef = Math.max(64, limit * 8);
  const _profile = (await import('../embed/profile-router.js')).then; // tree-shake guard
  const { readProfile } = await import('../embed/profile-router.js');
  const readProf = await readProfile(db);
  const embeddingTbl = embeddingTable(readProf, surface);
  const qvec = Array.from(await embedder.embed(query));

  // Step 1: HNSW kNN on the per-surface embeddings table.
  // TIMEOUT 2s is defensive — bounds runaway HNSW exploration if the index
  // is in a pathological state. Recall is fail-soft from the hook side, so
  // a timeout produces an empty hit list rather than crashing the turn.
  const knnSql = `SELECT record, vector::distance::knn() AS dist
                  FROM ${embeddingTbl}
                  WHERE vector <|${limit}, ${ef}|> $qvec
                  ORDER BY dist
                  LIMIT ${limit}
                  TIMEOUT 2s`;
  const [knnRows] = await db.query(new BoundQuery(knnSql, { qvec })).collect();
  if (knnRows.length === 0) return { hits: [] };

  // Step 2: fetch records and apply scope/kind/tags filter.
  const ids = knnRows.map((r) => r.record);
  const idDist = new Map(knnRows.map((r) => [recordStringId(r.record), r.dist]));

  const filters = ['id IN $ids'];
  const bindings = { ids };
  if (surface === 'memos' && opts.kind) {
    bindings.kind = opts.kind;
    filters.push('kind = $kind');
  }
  if (surface === 'events' && opts.source) {
    bindings.source = opts.source;
    filters.push('source = $source');
  }
  if (surface === 'entities' && opts.type) {
    bindings.entitytype = opts.type;
    filters.push('type = $entitytype');
  }
  const scopes = opts.scopes;
  if (scopes === undefined && !opts.includeEphemeral) {
    // default: exclude ephemerals
    filters.push(
      `(scope = 'global' OR string::starts_with(scope, 'project:') OR string::starts_with(scope, 'integration:') OR scope = 'private')`,
    );
  } else if (Array.isArray(scopes) && !scopes.includes('*')) {
    bindings.scopes = scopes;
    filters.push('scope IN $scopes');
  }
  if (opts.tags && opts.tags.length > 0) {
    bindings.tags = opts.tags;
    filters.push('tags CONTAINSANY $tags');
  }
  if (opts.since) {
    bindings.since = new Date(opts.since);
    const tsField = surface === 'events' ? 'ts' : 'derived_at';
    filters.push(`${tsField} >= $since`);
  }

  const fetchSql = `SELECT * FROM ${surface} WHERE ${filters.join(' AND ')}`;
  const [rows] = await db.query(new BoundQuery(fetchSql, bindings)).collect();

  const hits = rows
    .map((row) => ({ record: row, distance: idDist.get(recordStringId(row.id)) ?? 1 }))
    .sort((a, b) => a.distance - b.distance);

  return { hits };
}

/**
 * Non-vector list (index-backed): used by lenses for chronological views.
 */
export async function listMemos(db, opts = {}) {
  const { kind, scope, since, until, limit = 50 } = opts;
  if (!Number.isInteger(limit) || limit < 1 || limit > 1000) {
    throw new Error(`listMemos: limit out of range [1,1000]: ${limit}`);
  }
  const filters = [];
  const bindings = {};
  if (kind) {
    filters.push('kind = $kind');
    bindings.kind = kind;
  }
  if (scope) {
    filters.push('scope = $scope');
    bindings.scope = scope;
  }
  if (since) {
    filters.push('derived_at >= $since');
    bindings.since = new Date(since);
  }
  if (until) {
    filters.push('derived_at <= $until');
    bindings.until = new Date(until);
  }
  const where = filters.length > 0 ? `WHERE ${filters.join(' AND ')}` : '';
  const sql = `SELECT * FROM memos ${where} ORDER BY last_active DESC LIMIT ${limit}`;
  const [rows] = await db.query(new BoundQuery(sql, bindings)).collect();
  return rows;
}

/**
 * Bidirectional neighbor lookup along an edge kind.
 * For symmetric kinds, returns the *other* endpoint regardless of direction.
 * For asymmetric kinds with `direction = 'out'` (default), returns `to`s of
 * outbound edges; `direction = 'in'` returns `from`s of inbound; `'both'`
 * returns the union.
 */
export async function neighbors(db, recordId, kind, opts = {}) {
  const { limit = 10, direction = 'out' } = opts;
  if (!Number.isInteger(limit) || limit < 1 || limit > 1000) {
    throw new Error(`neighbors: limit out of range [1,1000]: ${limit}`);
  }
  const spec = EDGE_KIND_REGISTRY[kind];
  if (!spec) throw new Error(`neighbors: unknown kind ${kind}`);
  let whereClause;
  if (spec.symmetric) {
    whereClause = '(from = $e OR to = $e)';
  } else if (direction === 'out') {
    whereClause = 'from = $e';
  } else if (direction === 'in') {
    whereClause = 'to = $e';
  } else {
    whereClause = '(from = $e OR to = $e)';
  }
  const sql = `SELECT
    IF from = $e THEN to ELSE from END AS other,
    weight, last_seen, valid_from, valid_until
    FROM edges
    WHERE kind = $k AND ${whereClause}
    ORDER BY weight DESC
    LIMIT ${limit}`;
  const [rows] = await db.query(new BoundQuery(sql, { e: recordId, k: kind })).collect();
  return rows;
}

