/**
 * Upsert an entity by name (exact + embedding + LLM disambig cascade).
 * Current implementation delegates to the existing 3-stage cascade in
 * src/graph/stage*.js; this primitive exists so all writes route through
 * one module. The embedding write moves to the per-surface entities table.
 */
export async function upsertEntity(db, embedder, input) {
  const { upsertEntityCascade } = await import('../graph/upsert-entity.js');
  const result = await upsertEntityCascade(db, embedder, input);
  // Write embedding into the active profile's entities surface.
  if (result.id && result.embedding_source) {
    await writeEmbedding(db, embedder, 'entities', result.id, result.embedding_source);
  }
  return result;
}
