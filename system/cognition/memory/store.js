/**
 * Capture a raw event into the events firehose.
 *
 * Writes events row + embedding row (in the active profile's events surface)
 * in two queries — one transaction not strictly required because embedding
 * dedupe is keyed on event ID, which is generated by SurrealDB on insert.
 *
 * @returns {{ id, deduped: boolean }}
 */
export async function remember(db, embedder, input) {
  const {
    source,
    content,
    trust = 'trusted',
    scope = 'global',
    tags = [],
    attachments = [],
    meta,
    external_id,
  } = input;
  if (!source) throw new Error('remember: source required');
  if (!content || content.length === 0) throw new Error('remember: content required');
  validateScope(scope);

  // Validate attachments (open enum, but known kinds get their shape checked).
  for (const a of attachments) {
    const v = validateAttachment(a);
    if (!v.ok) throw new Error(`remember: attachment invalid: ${v.errors.join('; ')}`);
  }

  const contentHash = sha256(content);

  // Dedupe by content_hash within source — keeps integration syncs idempotent.
  const [existing] = await db
    .query(
      surql`SELECT id FROM events WHERE content_hash = ${contentHash} AND source = ${source} LIMIT 1`,
    )
    .collect();
  if (existing[0]) return { id: existing[0].id, deduped: true };

  // Normalize top-level `external_id` (sync convenience) into meta.external_id
  // — the events schema dropped the top-level column in the redesign.
  const normalizedMeta = external_id != null ? { ...(meta ?? {}), external_id } : meta;

  const fields = {
    source,
    content,
    content_hash: contentHash,
    trust,
    scope,
    tags,
    attachments,
    ...(normalizedMeta ? { meta: normalizedMeta } : {}),
  };
  const [created] = await db.query(surql`CREATE events CONTENT ${fields}`).collect();
  const row = Array.isArray(created) ? created[0] : created;
  const eventId = row.id;

  // Embedding failure must not lose the event. Mirrors recordEvent.
  try {
    await writeEmbedding(db, embedder, 'events', eventId, content);
  } catch (e) {
    console.warn(`store.remember: embedding failed for ${eventId}: ${e.message}`);
  }

  return { id: eventId, deduped: false };
}

/**
 * Create a memo of any kind. Subjects → `about` edges, lineage → `derived_from`
 * edges, both written via relateAll in the same call site.
 *
 * @returns {{ id, deduped: boolean }}
 */
export async function note(db, embedder, kind, input) {
  if (!kind) throw new Error('note: kind required');
  const v = validateMemoKind(kind, input);
  if (!v.ok) throw new Error(`note(${kind}): ${v.errors.join('; ')}`);
  const {
    content,
    subjects = [],
    confidence,
    scope = 'global',
    tags = [],
    derived_by,
    lineage = [],
    meta = {},
    derived_from_trust,
  } = input;

  validateScope(scope);
  const spec = MEMO_KIND_REGISTRY[kind];
  const dedupByHash = spec?.dedup_by === 'content_hash';
  const contentHash = sha256(content);

  if (dedupByHash) {
    const [existing] = await db
      .query(
        surql`SELECT id FROM memos WHERE kind = ${kind} AND content_hash = ${contentHash} LIMIT 1`,
      )
      .collect();
    if (existing[0]) return { id: existing[0].id, deduped: true };
  }

  const fields = {
    kind,
    content,
    content_hash: contentHash,
    derived_by,
    scope,
    tags,
    meta,
    ...(confidence !== undefined ? { confidence } : {}),
    ...(derived_from_trust != null ? { derived_from_trust } : {}),
  };
  const [created] = await db.query(surql`CREATE memos CONTENT ${fields}`).collect();
  const row = Array.isArray(created) ? created[0] : created;
  const memoId = row.id;

  // Embedding failure must not lose the memo. Mirrors recordEvent: the memo
  // row is already created above; if the embedder is mis-configured or
  // produces a vector the active table's schema rejects (e.g. mock 1024-d
  // vector vs gemini-3072 profile), log a warning and continue. Recall by
  // semantic search will be degraded until the row is back-filled, but
  // writes don't bubble InternalError to MCP clients.
  try {
    await writeEmbedding(db, embedder, 'memos', memoId, content);
  } catch (e) {
    console.warn(`store.note: embedding failed for ${memoId}: ${e.message}`);
  }

  // Emit `about` edges for subjects (memo → entity).
  if (subjects.length > 0) {
    const rows = subjects.map((s) => ({ from: memoId, to: s, kind: 'about' }));
    await relateAll(db, rows);
  }
  // Emit `derived_from` edges for lineage entries.
  if (lineage.length > 0) {
    const rows = lineage.map((l) => ({ from: memoId, to: l.id ?? l, kind: 'derived_from' }));
    await relateAll(db, rows);
  }

  return { id: memoId, deduped: false };
}

/**
 * Upsert a memo by its `meta.name`. Used by habits today; the primitive is
 * generic (any kind that carries `meta.name`). Increments `signal_count` and
 * refreshes `last_active` on match; creates otherwise.
 *
 * @returns {{ id, signal_increment: 0 | 1 }}
 */
export async function upsertMemoByName(db, embedder, kind, input) {
  const { name } = input;
  if (!name) throw new Error('upsertMemoByName: meta.name required (passed as input.name)');
  // SELECT-then-CREATE is not strictly atomic. Wrap in withTxRetry so the
  // SurrealDB engine's "Transaction conflict" surface for paired callers is
  // retried with jittered backoff; concurrent callers converge to one row.
  return withTxRetry(async () => {
    const [existing] = await db
      .query(surql`SELECT id FROM memos WHERE kind = ${kind} AND meta.name = ${name} LIMIT 1`)
      .collect();
    if (existing[0]) {
      const id = toRecordRef(existing[0].id);
      await db
        .query(surql`UPDATE ${id} SET signal_count += 1, last_active = time::now()`)
        .collect();
      // Also merge any new lineage as derived_from edges.
      if (input.lineage?.length > 0) {
        const rows = input.lineage.map((l) => ({
          from: id,
          to: l.id ?? l,
          kind: 'derived_from',
        }));
        await relateAll(db, rows);
      }
      return { id, signal_increment: 1 };
    }
    // Create path: ensure `meta.name` is part of the meta we pass through.
    const meta = { ...(input.meta ?? {}), name };
    const result = await note(db, embedder, kind, { ...input, meta });
    return { id: result.id, signal_increment: 0 };
  });
}

/**
 * Relate two records with a typed edge. Registry-validated. Self-loops
 * rejected. Symmetric kinds canonicalize endpoint order. Composite-ID UPSERT
 * for idempotence; counter kinds use `SET weight += 1`.
 */
export async function relate(db, from, to, kind, opts = {}) {
  const v = validateEdge(from, to, kind);
  if (!v.ok) throw new Error(`relate: ${v.errors.join('; ')}`);
  const [cFrom, cTo] = canonicalEndpoints(from, to, kind);
  const counter = isCounterKind(kind);

  // Build a deterministic composite ID. SurrealDB v3 accepts record-as-id-element
  // via the array literal syntax — we use BoundQuery here so the parser sees
  // literal record refs in the ID position.
  const fromIdStr = recordStringId(cFrom);
  const toIdStr = recordStringId(cTo);

  // Coerce strings to RecordId before binding — see comment in relateAll.
  const inRec = recordIdFromString(cFrom);
  const outRec = recordIdFromString(cTo);

  if (counter) {
    // TYPE RELATION tables don't accept UPSERT/CREATE for new rows; use
    // INSERT RELATION ... ON DUPLICATE KEY UPDATE for idempotent counter
    // increments. Bind params are $inRec/$outRec to avoid $in/$out clashing
    // with SurrealQL field-name parsing.
    const sql = `INSERT RELATION INTO edges {
      id: [$kind, $inRec, $outRec],
      in: $inRec, out: $outRec, kind: $kind, weight: 1, last_seen: time::now()
    } ON DUPLICATE KEY UPDATE
      weight += 1, last_seen = time::now()`;
    const [ret] = await db
      .query(
        new BoundQuery(sql, {
          kind,
          inRec,
          outRec,
        }),
      )
      .collect();
    const row = Array.isArray(ret) ? ret[0] : ret;
    return { id: row?.id };
  }

  const bindings = { kind, inRec, outRec };
  // Build the insert-content map and the on-duplicate update set in parallel,
  // so optional fields like weight/valid_from/etc. apply on both code paths.
  const insertFields = [
    'id: [$kind, $inRec, $outRec]',
    'in: $inRec',
    'out: $outRec',
    'kind: $kind',
    'last_seen: time::now()',
  ];
  const updateExprs = ['kind = $kind', 'last_seen = time::now()'];
  for (const [field, val] of [
    ['weight', opts.weight],
    ['valid_from', opts.valid_from],
    ['valid_until', opts.valid_until],
    ['context', opts.context],
    ['meta', opts.meta],
    ['derived_from_trust', opts.derived_from_trust],
  ]) {
    if (val !== undefined && val !== null) {
      bindings[field] = val;
      insertFields.push(`${field}: $${field}`);
      updateExprs.push(`${field} = $${field}`);
    }
  }
  const sql = `INSERT RELATION INTO edges { ${insertFields.join(', ')} }
    ON DUPLICATE KEY UPDATE ${updateExprs.join(', ')}`;
  const [ret] = await db.query(new BoundQuery(sql, bindings)).collect();
  const row = Array.isArray(ret) ? ret[0] : ret;

  return { id: row?.id, fromId: fromIdStr, toId: toIdStr };
}

/**
 * Bulk relate. Builds one multi-statement query so a 20-edge biographer fan-out
 * costs 1 round-trip instead of 20. Chunks at 50 to avoid pathological parser
 * limits. Wrapped in BEGIN/COMMIT so a malformed edge doesn't leave partial state.
 */
const RELATE_CHUNK = 50;
// Per-slice transaction-conflict retry. Keeps a smaller cap than the outer
// withTxRetry (4) because relateAll is hot-path and slices are independent —
// 2 retries with jittered backoff resolves the common race-on-same-edge case
// without amplifying latency on terminal errors.
const TX_CONFLICT_MAX_RETRIES = 2;
export async function relateAll(db, rows) {
  if (!Array.isArray(rows) || rows.length === 0) return { ids: [] };
  // Pre-filter invalid edges (self-loops, unknown kinds, wrong endpoint tables)
  // and log each skip. The biographer pipeline routinely produces small batches
  // that include 1-2 redundant self-loops (LLM aliasing two synonymous names to
  // the same canonical entity); failing the whole batch on a single invalid
  // edge means we silently drop *all* of the surrounding good edges for the
  // event — a much worse outcome than just dropping the bad ones.
  const valid = [];
  for (let i = 0; i < rows.length; i++) {
    const row = rows[i];
    const v = validateEdge(row.from, row.to, row.kind);
    if (!v.ok) {
      console.warn(`relateAll[${i}]: skipping invalid edge — ${v.errors.join('; ')}`);
      continue;
    }
    valid.push(row);
  }
  if (valid.length === 0) return { ids: [] };
  const ids = [];
  for (let off = 0; off < valid.length; off += RELATE_CHUNK) {
    const slice = valid.slice(off, off + RELATE_CHUNK);
    const stmts = ['BEGIN TRANSACTION'];
    const bindings = {};
    slice.forEach((row, i) => {
      const [cIn, cOut] = canonicalEndpoints(row.from, row.to, row.kind);
      bindings[`k${i}`] = row.kind;
      // SurrealDB's `INSERT RELATION INTO edges { in: $i, out: $o }` requires
      // `in` / `out` to bind as Thing (RecordId) values, not plain strings.
      // A string binding ("entities:foo") triggers
      //   "Cannot execute INSERT statement where property 'in' is: 'entities:foo'"
      // — and because each slice runs inside one BEGIN/COMMIT, the failure
      // cascades into "query was not executed due to a failed transaction"
      // for every following row, dropping the entire 50-edge chunk.
      // `recordIdFromString` is a pass-through for non-strings, so safe to
      // apply unconditionally regardless of which caller (biographer,
      // ingest, v1-import) produced the row.
      bindings[`i${i}`] = recordIdFromString(cIn);
      bindings[`o${i}`] = recordIdFromString(cOut);
      const insertFields = [
        `id: [$k${i}, $i${i}, $o${i}]`,
        `in: $i${i}`,
        `out: $o${i}`,
        `kind: $k${i}`,
        `last_seen: time::now()`,
      ];
      const updateExprs = [`kind = $k${i}`, `last_seen = time::now()`];
      if (isCounterKind(row.kind)) {
        insertFields.push(`weight: 1`);
        updateExprs.push(`weight += 1`);
      } else {
        for (const f of [
          'weight',
          'valid_from',
          'valid_until',
          'context',
          'meta',
          'derived_from_trust',
        ]) {
          if (row[f] != null) {
            bindings[`${f}${i}`] = row[f];
            insertFields.push(`${f}: $${f}${i}`);
            updateExprs.push(`${f} = $${f}${i}`);
          }
        }
      }
      stmts.push(
        `INSERT RELATION INTO edges { ${insertFields.join(', ')} } ON DUPLICATE KEY UPDATE ${updateExprs.join(', ')}`,
      );
    });
    stmts.push('COMMIT TRANSACTION');
    // Per-slice try/catch: a transaction reject on one slice (e.g. SurrealDB
    // refusing a RecordId with special chars in the bound `out` field) used
    // to bubble up and kill the entire relateAll batch, dropping every other
    // valid edge. Each slice has its own BEGIN/COMMIT, so a failure here is
    // already atomically scoped to just this 50-edge chunk.
    //
    // Transaction conflicts (concurrent writer racing the same edge) are
    // retryable — fail-and-skip would drop ~25 edges per conflict, and they
    // recur under sustained sync load. Retry transient conflicts a few times
    // with jittered backoff before giving up; non-conflict errors (parser /
    // schema reject) skip the slice immediately as before.
    let sliceErr = null;
    for (let attempt = 0; attempt <= TX_CONFLICT_MAX_RETRIES; attempt++) {
      try {
        const results = await db.query(new BoundQuery(stmts.join(';\n'), bindings)).collect();
        for (const r of results) {
          const row = Array.isArray(r) ? r[0] : r;
          if (row?.id) ids.push(row.id);
        }
        sliceErr = null;
        break;
      } catch (e) {
        sliceErr = e;
        if (!isTxConflict(e)) break;
        await awaitTxBackoff();
      }
    }
    if (sliceErr) {
      const suffix = isTxConflict(sliceErr) ? ' (retried, still conflict)' : '';
      console.warn(
        `relateAll: slice [${off}..${off + slice.length - 1}] failed, skipping ${slice.length} edges${suffix}: ${sliceErr.message}`,
      );
    }
  }
  return { ids };
}

/**
 * Add a `supersedes` edge new_memo → old_memo. fn::freshness will return 0
 * for old_memo from now on (per its inbound supersedes check). The old memo
 * row is NOT updated/deleted — historical readings remain queryable.
 */
export async function supersede(db, oldId, newId) {
  const { id } = await relate(db, newId, oldId, 'supersedes');
  return { superseded: oldId, by: newId, edge: id };
}

/**
 * Mark two memos as contradicting each other (symmetric, both visible).
 */
export async function flagContradiction(db, idA, idB, opts = {}) {
  return relate(db, idA, idB, 'contradicts', opts);
}

/**
 * Update a memo's `meta` field by merging in a patch. Used by foresight to
 * record prediction resolution (resolved_at / correct / actual_outcome).
 */
export async function updateMemoMeta(db, id, patch) {
  await db.query(surql`UPDATE ${toRecordRef(id)} MERGE { meta: ${patch} }`).collect();
}

