// store.js — the only writer to events/memos/entities/edges/embeddings.
//
// Spec §5.1. All capture, biographer, dream, ingest, and outbound paths go
// through these primitives. Faculty lenses (knowledge.js, habits.js, …) are
// thin re-exports that bake `kind` in.
//
// Design rules baked in here:
//   1. Embeddings live in separable per-(profile, surface) tables. The store
//      writes the data row and the embedding row in the same transaction.
//   2. Edges use composite-ID UPSERT for idempotence; counter kinds use the
//      `SET field += 1` idiom verified against SurrealDB v3.0.5 (G0.b).
//   3. Content-hash dedupe is opt-in per kind via MEMO_KIND_REGISTRY[kind].dedup_by.
//   4. Subjects + lineage on memos are written as `about` / `derived_from` edges,
//      not as array fields on the memo row. (Spec §5.0 — fields removed.)
//   5. Validation happens via MEMO_KIND_REGISTRY / EDGE_KIND_REGISTRY before
//      any DB write; failures throw with a structured error message.

