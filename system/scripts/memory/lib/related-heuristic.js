// related-heuristic.js — Pass 3 logic for densify-wiki.
// Builds entity-mention matrix, generates cross-directory pairs with
// sub-tree dampening + super-hub filter, writes symmetric `related:`
// edges with hand-curated preservation.
//
// Threshold is evaluated on the UNFILTERED shared-entity count so that
// removing globally-ubiquitous hub slugs (the super-hub filter) doesn't
// inadvertently drop pairs that had sufficient real overlap.

const FM_BLOCK_RE = /^---\n[\s\S]*?\n---\n?/;
const CODE_FENCE_RE = /```[\s\S]*?```/g;

function stripIgnoredRegions(body) {
  return body.replace(FM_BLOCK_RE, '').replace(CODE_FENCE_RE, '');
}

function escapeRegex(s) {
  return s.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}

const DEFAULT_EXCLUDED_PREFIXES = [
  'archive/',
  'quarantine/',
  'knowledge/conversations/',
  'knowledge/calendar/events/',
  'knowledge/finance/lunch-money/transactions/',
];

function parentDir(relPath) {
  const parts = relPath.split('/');
  return parts.slice(0, -1).join('/');
}

function isExcluded(relPath, excluded) {
  return excluded.some(prefix => relPath.startsWith(prefix));
}

export function generatePairs(matrix, { excludedSubtrees } = {}) {
  const excluded = excludedSubtrees ?? DEFAULT_EXCLUDED_PREFIXES;
  const files = [...matrix.entries()].filter(([rel]) => !isExcluded(rel, excluded));
  const pairs = [];
  for (let i = 0; i < files.length; i++) {
    for (let j = i + 1; j < files.length; j++) {
      const [aPath, aSet] = files[i];
      const [bPath, bSet] = files[j];
      if (parentDir(aPath) === parentDir(bPath)) continue;
      const shared = new Set();
      for (const slug of aSet) {
        if (bSet.has(slug)) shared.add(slug);
      }
      if (shared.size === 0) continue;
      pairs.push({ a: aPath, b: bPath, sharedEntities: shared });
    }
  }
  return pairs;
}

export { DEFAULT_EXCLUDED_PREFIXES };

export function computeSuperHubs(matrix, { pct = 0.05 } = {}) {
  const counts = new Map();
  for (const set of matrix.values()) {
    for (const slug of set) counts.set(slug, (counts.get(slug) ?? 0) + 1);
  }
  const sorted = [...counts.entries()].sort((a, b) => b[1] - a[1]);
  const cutoff = Math.max(1, Math.ceil(sorted.length * pct));
  return new Set(sorted.slice(0, cutoff).map(([slug]) => slug));
}

export function applySuperHubFilter(pairs, hubs) {
  return pairs.map(p => {
    const filtered = new Set();
    for (const s of p.sharedEntities) {
      if (!hubs.has(s)) filtered.add(s);
    }
    return { ...p, sharedEntities: filtered };
  });
}

export function selectEdges(pairs, { threshold = 3, topK = 5, totalCap = 10, existing = new Map() } = {}) {
  const eligible = pairs.filter(p => p.sharedEntities.size >= threshold);

  // Group eligible pairs by source (a-side only) for top-K outbound selection.
  // Using only the 'a' side means pairs generated by generatePairs drive outbound
  // rankings from the first-encountered file's perspective; symmetry then propagates
  // back-edges to the 'b' side up to totalCap.
  const byFile = new Map();
  for (const p of eligible) {
    if (!byFile.has(p.a)) byFile.set(p.a, []);
    byFile.get(p.a).push({ target: p.b, overlap: p.sharedEntities.size });
  }

  // Top-K outbound per file (a-side perspective).
  const edges = new Map();
  for (const [file, neighbors] of byFile) {
    neighbors.sort((x, y) => y.overlap - x.overlap || x.target.localeCompare(y.target));
    const top = neighbors.slice(0, topK).map(n => n.target);
    edges.set(file, new Set(top));
  }

  // Symmetric: when A picked B, ensure B includes A (up to totalCap).
  for (const [file, set] of edges) {
    for (const target of set) {
      if (!edges.has(target)) edges.set(target, new Set());
      if (edges.get(target).size < totalCap) edges.get(target).add(file);
    }
  }

  // Set-union with hand-curated existing edges (never remove).
  for (const [file, existingSet] of existing) {
    if (!edges.has(file)) edges.set(file, new Set(existingSet));
    else for (const e of existingSet) edges.get(file).add(e);
  }

  return edges;
}

function isSelfReference(relPath, slug) {
  // Avoid a page counting itself as a mention.
  // Works for both simple slugs (jake-lee) and path slugs (profile/people/jake-lee).
  const withoutExt = relPath.replace(/\.md$/, '');
  return withoutExt === slug || withoutExt.endsWith(`/${slug}`);
}

export function buildMentionMatrix(filesMap, registry) {
  const matrix = new Map();
  for (const [relPath, body] of filesMap) {
    const cleaned = stripIgnoredRegions(body);
    const found = new Set();
    for (const { slug, aliases } of registry) {
      if (isSelfReference(relPath, slug)) continue;
      for (const alias of aliases) {
        const re = new RegExp(`\\b${escapeRegex(alias)}\\b`, 'i');
        if (re.test(cleaned)) {
          found.add(slug);
          break;
        }
      }
    }
    matrix.set(relPath, found);
  }
  return matrix;
}

import { readFileSync, readdirSync, existsSync } from 'node:fs';
import { join, relative } from 'node:path';
import { atomicWrite } from '../../jobs/lib/atomic.js';
import { buildEntityRegistry } from '../../wiki-graph/lib/build-entity-registry.js';

const RELATED_RE = /^related:\s*\[([^\]]*)\]\s*$/m;
const FM_BLOCK = /^(---\n[\s\S]*?\n---\n?)/;

function* walkMdSync(root, base = root) {
  if (!existsSync(root)) return;
  for (const entry of readdirSync(root, { withFileTypes: true })) {
    const full = join(root, entry.name);
    if (entry.isDirectory()) yield* walkMdSync(full, base);
    else if (entry.isFile() && entry.name.endsWith('.md')) {
      yield relative(base, full).split(/[\\/]/).join('/');
    }
  }
}

function getRelated(body) {
  const m = body.match(RELATED_RE);
  if (!m) return [];
  return m[1].split(',').map(s => s.trim()).filter(Boolean);
}

function setRelated(body, list) {
  const line = `related: [${list.join(', ')}]`;
  const fm = body.match(FM_BLOCK);
  if (!fm) return `---\n${line}\n---\n${body}`;
  let block = fm[1];
  block = RELATED_RE.test(block)
    ? block.replace(RELATED_RE, line)
    : block.replace(/\n---\n?$/, `\n${line}\n---\n`);
  return block + body.slice(fm[1].length);
}

export async function runRelatedHeuristic({
  workspaceDir,
  threshold = 3,
  topK = 5,
  totalCap = 10,
  superhubPct = 0.05,
  dryRun = false,
}) {
  const memoryRoot = join(workspaceDir, 'user-data', 'memory');
  const { byPath } = await buildEntityRegistry(workspaceDir);

  // Convert byPath into [{slug, aliases}] for buildMentionMatrix.
  // Use relPath without .md as slug so each entity-bearing page has a unique
  // identity in the mention matrix (avoids collisions across directories).
  const registry = [...byPath.entries()].map(([relPath, info]) => ({
    slug: relPath.replace(/\.md$/, ''),
    aliases: info.aliases,
  }));

  const filesMap = new Map();
  const existingRelated = new Map();
  for (const relPath of walkMdSync(memoryRoot)) {
    const body = readFileSync(join(memoryRoot, relPath), 'utf-8');
    filesMap.set(relPath, body);
    const r = getRelated(body);
    if (r.length) existingRelated.set(relPath, new Set(r));
  }

  const matrix = buildMentionMatrix(filesMap, registry);
  const hubs = computeSuperHubs(matrix, { pct: superhubPct });

  // Generate pairs and apply threshold on the UNFILTERED shared count so that
  // super-hub removal doesn't disqualify pairs with sufficient real overlap.
  const allPairs = generatePairs(matrix, {});
  const pairs = allPairs.filter(p => p.sharedEntities.size >= threshold);
  const filteredPairs = applySuperHubFilter(pairs, hubs);

  // Pass threshold=0 because we already pre-filtered above (threshold on original count).
  const edges = selectEdges(filteredPairs, { threshold: 0, topK, totalCap, existing: existingRelated });

  let edgesAdded = 0;
  let filesModified = 0;
  for (const [relPath, set] of edges) {
    const before = existingRelated.get(relPath) ?? new Set();
    const newOnly = [...set].filter(e => !before.has(e));
    if (newOnly.length === 0) continue;
    const filePath = join(memoryRoot, relPath);
    const body = filesMap.get(relPath);
    if (!body) continue;
    const merged = [...new Set([...before, ...set])];
    const newBody = setRelated(body, merged);
    if (!dryRun) atomicWrite(filePath, newBody);
    edgesAdded += newOnly.length;
    filesModified += 1;
  }

  return {
    summary: {
      edgesAdded,
      filesModified,
      superHubs: [...hubs],
      pairsConsidered: allPairs.length,
      pairsAfterFilter: filteredPairs.length,
    },
  };
}
