// External-skill loader. Parses SKILL.md frontmatter (custom YAML-lite,
// matching system/scripts/lib/protocol-frontmatter.js), validates skills,
// scans user-data/skills/external/, and maintains the INDEX.md +
// installed-skills.json manifest.
//
// External skills coexist with Robin's existing system/jobs/ protocols. This
// loader does NOT touch system/jobs/. See
// docs/superpowers/specs/2026-05-04-external-skill-compat-layer.md.

import { readFileSync, existsSync, readdirSync, statSync, writeFileSync, renameSync, mkdirSync } from 'node:fs';
import { basename, dirname, join } from 'node:path';

const FM_RE = /^---\n([\s\S]*?)\n---\n?/;

export function parseSkillFrontmatter(content) {
  const m = content.match(FM_RE);
  if (!m) return { frontmatter: {}, body: content };
  const frontmatter = {};
  const lines = m[1].split('\n');
  let i = 0;
  while (i < lines.length) {
    const line = lines[i];
    const kv = line.match(/^([\w-]+):\s*(.*)$/);
    if (!kv) { i++; continue; }
    const [, key, raw] = kv;
    const trimmed = raw.trim();
    // Block-list form: `key:\n  - val\n  - val`
    if (trimmed === '' && i + 1 < lines.length && /^\s+- /.test(lines[i + 1])) {
      const arr = [];
      i++;
      while (i < lines.length && /^\s+- /.test(lines[i])) {
        const item = lines[i].replace(/^\s+- /, '').trim();
        const sm = item.match(/^["'](.*)["']$/);
        arr.push(sm ? sm[1] : item);
        i++;
      }
      frontmatter[key] = arr;
      continue;
    }
    frontmatter[key] = parseValue(trimmed);
    i++;
  }
  return { frontmatter, body: content.slice(m[0].length) };
}

function parseValue(raw) {
  if (raw === 'true') return true;
  if (raw === 'false') return false;
  if (/^-?\d+$/.test(raw)) return Number(raw);
  if (/^"(.*)"$/.test(raw)) return raw.slice(1, -1);
  if (/^'(.*)'$/.test(raw)) return raw.slice(1, -1);
  // Inline array: ["a", "b"]
  const arr = raw.match(/^\[(.*)\]$/);
  if (arr) {
    return arr[1]
      .split(',')
      .map((s) => {
        const t = s.trim();
        const sm = t.match(/^["'](.*)["']$/);
        return sm ? sm[1] : t;
      })
      .filter((s) => s.length > 0);
  }
  return raw;
}

// validateSkill(folderPath) → { ok: true, skill } | { ok: false, reason }
export function validateSkill(folderPath) {
  const skillFile = join(folderPath, 'SKILL.md');
  if (!existsSync(skillFile)) {
    return { ok: false, reason: `SKILL.md not found at ${skillFile}` };
  }
  let content;
  try {
    content = readFileSync(skillFile, 'utf8');
  } catch (err) {
    return { ok: false, reason: `Failed to read SKILL.md: ${err.message}` };
  }
  const { frontmatter } = parseSkillFrontmatter(content);
  if (!frontmatter.name) {
    return { ok: false, reason: 'Frontmatter missing required field: name' };
  }
  if (!frontmatter.description) {
    return { ok: false, reason: 'Frontmatter missing required field: description' };
  }
  if (frontmatter.override !== undefined) {
    return { ok: false, reason: 'External skills must not declare `override:` (reserved for system + user-data overrides)' };
  }
  const folderName = basename(folderPath);
  if (frontmatter.name !== folderName) {
    return { ok: false, reason: `Frontmatter name "${frontmatter.name}" does not match folder name "${folderName}"` };
  }
  return {
    ok: true,
    skill: {
      name: frontmatter.name,
      description: frontmatter.description,
      triggerAliases: frontmatter['trigger-aliases'] || [],
      folderPath,
    },
  };
}

const EXTERNAL_REL = ['user-data', 'skills', 'external'];

export function externalDir(workspaceDir) {
  return join(workspaceDir, ...EXTERNAL_REL);
}

export function scanSkills(workspaceDir) {
  const dir = externalDir(workspaceDir);
  if (!existsSync(dir)) return [];
  const entries = readdirSync(dir);
  const skills = [];
  for (const entry of entries) {
    const folderPath = join(dir, entry);
    let st;
    try {
      st = statSync(folderPath);
    } catch {
      continue;
    }
    if (!st.isDirectory()) continue;
    const result = validateSkill(folderPath);
    if (result.ok) skills.push(result.skill);
    // TODO(skill-doctor): surface rejected folders + reasons (handled by `robin skill doctor` in Task 11).
  }
  return skills.sort((a, b) => a.name.localeCompare(b.name));
}

const INDEX_HEADER = `# External skills installed
<!-- auto-generated by external-skill-loader; do not edit by hand -->

`;

export function generateIndex(workspaceDir) {
  const dir = externalDir(workspaceDir);
  mkdirSync(dir, { recursive: true });
  const skills = scanSkills(workspaceDir);
  let body;
  if (skills.length === 0) {
    body = '_no skills installed_\n';
  } else {
    body = skills.map((s) => {
      const desc = truncate(s.description, 200);
      const triggers = s.triggerAliases.length > 0
        ? ` Triggered by ${s.triggerAliases.map((t) => `"${t}"`).join(', ')}.`
        : '';
      const bodyPath = `user-data/skills/external/${s.name}/SKILL.md`;
      return `- **${s.name}** — ${desc}.${triggers} Body: ${bodyPath}`;
    }).join('\n') + '\n';
  }
  const final = INDEX_HEADER + body;
  // Atomic write: write to .tmp then rename.
  const indexPath = join(dir, 'INDEX.md');
  const tmpPath = indexPath + '.tmp';
  writeFileSync(tmpPath, final);
  renameSync(tmpPath, indexPath);
  return { skillCount: skills.length, path: indexPath };
}

function truncate(s, max) {
  const trimmed = s.replace(/\.+$/, '');
  return trimmed.length <= max ? trimmed : trimmed.slice(0, max - 1) + '…';
}

const MANIFEST_REL = ['user-data', 'runtime', 'state', 'installed-skills.json'];

export function manifestPath(workspaceDir) {
  return join(workspaceDir, ...MANIFEST_REL);
}

function defaultManifest() {
  return { schemaVersion: 1, skills: [] };
}

export function loadInstalledManifest(workspaceDir) {
  const p = manifestPath(workspaceDir);
  if (!existsSync(p)) return defaultManifest();
  try {
    const data = JSON.parse(readFileSync(p, 'utf8'));
    if (!data || typeof data !== 'object') return defaultManifest();
    if (typeof data.schemaVersion !== 'number') data.schemaVersion = 1;
    if (!Array.isArray(data.skills)) data.skills = [];
    return data;
  } catch {
    return defaultManifest();
  }
}

export function writeInstalledManifest(workspaceDir, manifest) {
  const p = manifestPath(workspaceDir);
  mkdirSync(dirname(p), { recursive: true });
  writeFileSync(p, JSON.stringify(manifest, null, 2) + '\n');
}

export function addManifestEntry(workspaceDir, entry) {
  const m = loadInstalledManifest(workspaceDir);
  const i = m.skills.findIndex((s) => s.name === entry.name);
  if (i >= 0) m.skills[i] = entry;
  else m.skills.push(entry);
  m.skills.sort((a, b) => a.name.localeCompare(b.name));
  writeInstalledManifest(workspaceDir, m);
  return m;
}

export function removeManifestEntry(workspaceDir, name) {
  const m = loadInstalledManifest(workspaceDir);
  m.skills = m.skills.filter((s) => s.name !== name);
  writeInstalledManifest(workspaceDir, m);
  return m;
}
